# Program Kalor Lebur Es

while True:
    print('\n\033[92m==================================')
    print('\033[31m      PROGRAM KALOR LEBUR ES       ')
    print('\033[92m==================================')

    m = input('\033[33mMasukan Massa Benda (Kg): ')
    l = input('\033[33mMasukan Kalor Lebur Es (J/Kg): ')

    hasil = int(m)*int(l)

    print(f'\n\033[37mHasilnya Adalah: {round (hasil,2)} J')